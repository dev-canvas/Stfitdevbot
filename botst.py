@dp.message(StateFilter(AdminStates.waiting_time_delete), F.text)
async def process_time_delete(msg: Message, state: FSMContext):
    """Обработка удаления времени"""
    if msg.from_user.id != ADMIN_ID:
        return
    
    async with aiosqlite.connect(DB_PATH) as db:
        cursor = await db.execute("DELETE FROM schedule WHERE post_time = ?", (msg.text.strip(),))
        await db.commit()
        
        if cursor.rowcount > 0:
            await load_schedule()
            await msg.answer(f"✅ Удалено время {msg.text.strip()}", reply_markup=get_main_keyboard())
        else:
            await msg.answer("❌ Такого времени нет в расписании!")
    
    await state.clear()


